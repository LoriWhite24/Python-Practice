"""
 Notes on Data Types in Python
"""
var = 1
print(var)
Var = 2.0 
# Concatenating Strings and Numerics
print("This is what Var is holding: " + str(Var))
# Type casting
print(float(var))
print(int(Var))
print(type(var))
# The Numeric Data Types
print("2 + 2 =", 2 + 2)
print("4 - 2 =", 4 - 2)
print("2 * 2 =", 2 * 2)
print("4 / 2 =", 4 / 2)
print("4 % 3 =", 4 % 3)
# Data and thier Types
myVar = 1
print(myVar, "is of data type", type(myVar))
myVar = 3.14
print(myVar, "is of data type", type(myVar))
myVar = 5j
print(myVar, "is of data type", type(myVar))
myVar = True
print(myVar, "is of data type", type(myVar))